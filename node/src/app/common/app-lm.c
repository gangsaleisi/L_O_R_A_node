    LoRaMacCallbacks.MacEvent = OnMacEvent;
    LoRaMacCallbacks.GetBatteryLevel = NULL;
    LoRaMacInit( &LoRaMacCallbacks );

    // Random seed initialization
    srand1( RAND_SEED );
    // Choose a random device address
    // NwkID = 0
